package cursoJava2024;

public class While {
    public static void main(String[] args) {
        // Declarei uma variável:
        int numero = 45;
        
        // Loop while que imprime números de 0 a 10
        while (numero <= 159) {
            System.out.print(numero + " "); // Exibe o número com um espaço
            numero = numero + 1; // Incrementa o número (ou pode ser numero++)
        }
    }
}
